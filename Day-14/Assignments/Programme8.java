/*Q8. Define three int variables for three numbers, calculate their average, and print it.*/
class Programme8{
	public static void main(String args[]){
		int a,b,c,total, average;
		a = 10;
		b = 15;
		c = 20;
		total = a+b+c;
		average = total/2;
		System.out.println("Total of a, b and c is" + total); // Output - 45
		System.out.println("Average of total is" + average); // Output - 22
		
	}
}